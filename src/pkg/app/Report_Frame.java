    private void retrieve_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_retrieve_btnActionPerformed

        textField1 = textField1_txt.getText();
        textField2 = textField2_txt.getText();

        if (model.getRowCount() > 0) {
            model.setRowCount(0);
        }
        seekingJob_lbl.setText("Seeking : 0");
        employed_lbl.setText("Employed : 0");
        studying_lbl.setText("Studying : 0");

        if (numberOfSeeking > 0) {
            numberOfSeeking = 0;
        }

        if (numberOfEmployed > 0) {
            numberOfEmployed = 0;
        }

        if (numberOfStudying > 0) {
            numberOfStudying = 0;
        }

        if (studentID_rdbtn.isSelected()) {
            dataRetrieval(textField1, "Student ID");
        } else if (name_rdbtn.isSelected()) {
            dataRetrieval(textField1, textField2, "First Name", "Family Name");
        } else if (school_rdbtn.isSelected()) {
            dataRetrieval(textField1, "School");
        } else if (major_rdbtn.isSelected()) {
            dataRetrieval(textField1, "Major");
        } else if (batch_rdbtn.isSelected()) {
            dataRetrieval(jComboBox1.getSelectedItem().toString(), "Graduation Year");
        }

        totalRows_lbl.setText("Total Rows : " + (model.getRowCount()));
        seekingJob_lbl.setText("Seeking Job : " + numberOfSeeking);
        employed_lbl.setText("Employed : " + numberOfEmployed);
        studying_lbl.setText("Studying : " + numberOfStudying);
    }//GEN-LAST:event_retrieve_btnActionPerformed

