    private void dataRetrieval(String s, String nameOfField) {

        Statement stmt = null;
        ResultSet rs1 = null;

        try {

            stmt = con.createStatement();
            rs1 = stmt.executeQuery("Select * From " + "All_Batches Where " + " All_Batches.[" + nameOfField + "] = " + "'" + s + "'");
            while (rs1.next()) {
                retrieve(rs1);
            }

            //JOptionPane.showMessageDialog(null, c);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }

    }

    private void dataRetrieval(String s, String s1, String nameOfField1, String nameOfField2) {

        Statement stmt = null;
        ResultSet[] rs = null;
        ResultSet rs1 = null;

        try {

            stmt = con.createStatement();
            rs1 = stmt.executeQuery("Select * From " + "All_Batches Where " + " All_Batches.[" + nameOfField1 + "] = '" + s + "'" + " And All_Batches.[" + nameOfField2 + "] = '" + s1 + "'");
            while (rs1.next()) {
                retrieve(rs1);
            }

            //JOptionPane.showMessageDialog(null, c);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }

    }

